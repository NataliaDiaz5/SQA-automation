package com.newtours.bookfligtht.pruebas.models;

public class User {

	String user;
	String password;
	public User(String user, String password) {
		
		this.user = user;
		this.password = password;
		
		
	}
	public final String getUser() {
		return user;
	}
	
	public final String getPassword() {
		return password;
	}
	
	
	
}
